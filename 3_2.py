num_1 = int(input('Введите первое число: '))
num_2 = int(input('Введите второе число: '))

print('{} + {} = '.format(num_1, num_2))
print(num_1 + num_2)

print('{} - {} = '.format(num_1, num_2))
print(num_1 - num_2)

print('{} * {} = '.format(num_1, num_2))
print(num_1 * num_2)

print('{} / {} = '.format(num_1, num_2))
print(num_1 / num_2)
