num_1 = int(input('введи 1-е число: '))
num_2 = int(input('введи 2-е число: '))
num_3 = int(input('введи 3-е число: '))
num_4 = int(input('введи 4-е число: '))
num_5 = int(input('введи 5-е число: '))
print(' {} + {} / {} - {} * {} = '.format(num_1, num_2, num_3, num_4, num_5))
print(num_1 + num_2 / num_3 - num_4 * num_5)

print(' {} * {} - {} / {} + {} = '.format(num_1, num_2, num_3, num_4, num_5))
print(num_1 * num_2 - num_3 / num_4 + num_5)

print(' {} / {} * {} + {} - {} = '.format(num_1, num_2, num_3, num_4, num_5))
print(num_1 / num_2 * num_3 + num_4 - num_5)

print(' {} - {} + {} * {} / {} = '.format(num_1, num_2, num_3, num_4, num_5))
print(num_1 - num_2 + num_3 * num_4 / num_5)
